package condition;

public class LoopEx_1 {
    public static void main(String[] args) {


        System.out.println("while 문");
        int i = 0;
        while (i<=10) {
            System.out.println("i의 값: " + i);
            i++;
        }
        System.out.println("for문");
        for (i = 0; i <= 10; i++) {
            System.out.println("i의값: " + i);
        }
    }
}
